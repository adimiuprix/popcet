                                <h5 class="mb-4 text-primary text-center">Crypto Technical Analysis: Definition, Principles </h5>
                                <img style="width: 100%; height: auto;" class="blog-card_img" src="https://sweetclaim.com/assets/upload/blog/post_id/18/87ecac81a676b744c302b28094d02c7a.jpg" alt="blog-thumb">
