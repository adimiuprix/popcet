                                <h5 class="fw-bold">Overview</h5>
                                <hr class="bg-primary">
                                <p>In trading cryptocurrencies , you need a variety of strategies to be able to profit and minimize risk. One of those ways is crypto technical analysis . To understand this analysis, there are unfamiliar terms that you need to understand.</p>
                                <p>However, take it easy, as long as you want to learn technical analysis, you can definitely understand it. Here will be explained about the technical analysis of cryptocurrencies and things related to it.</p>
                                <h2>What is Crypto Technical Analysis ?</h2>
                                <p><br>Crypto technical analysis is a method used to assess a crypto asset in terms of its price movement, so that you can make the right decision .</p>
                                <p>How to analyze cryptocurrencies usually uses the help of charts. You can also see how the cryptocurrency price moves .</p>
                                <p>You must already know the principle of cryptocurrency trading , which is to buy when the price falls, and sell when the price increases. Well, this technical analysis of cryptocurrencies helps you buy and sell at the right time.</p>
                                <p>That’s because sometimes when the price of crypto is cheap, it turns out to be cheaper, and vice versa.</p>
                                <p>So that you don’t miss the right moment, you need to be able to read crypto graphics correctly.</p>
                                <h2>Fundamental Principles of Technical Analysis</h2>
                                <p><br>Studying technical analysis is a part of learning crypto that you need to go through. You need to understand the fundamental principles of technical analysis in order to use it properly.&nbsp;</p>
                                <ul>
                                    <li>Happens repeatedly</li>
                                    <li>Price is the main criterion</li>
                                    <li>Prices follow the current trend</li>
                                </ul>
                                <ol>
                                    <li><strong>Happens repeatedly</strong><br>The crypto market moves according to the sentiment of the world economy. This makes the reactions of traders and investors tend to be the same to these sentiments.</li>
                                </ol>
                                <p>You can directly predict cryptocurrency price movements from the current sentiment. Usually you will know psychologically when someone wants to buy and sell. This is a common thing that can be your all-time benchmark in the cryptocurrency market .</p>
                                <ol>
                                    <li><strong>Price becomes the main model</strong><br>Price movements are the main reference for crypto technical analysis . The reason why prices can go up and down, you don’t need to think too far.</li>
                                </ol>
                                <p>Moreover, the price is the end result of various factors that cause price movements to vary. The most important thing is that you read the chart correctly.&nbsp;</p>
                                <ol>
                                    <li><strong>Prices follow the trend</strong><br>Trends are an important part of crypto technical analysis . In this case, we can take the example of the trend experienced by <a href="https://www.wiour.com/get-to-know-the-4-types-of-bitcoin-wallets/">Bitcoin</a> which can also influence the overall trend of the crypto market.</li>
                                </ol>
                                <p>If you manage to monitor trends well, you will not be careless in making decisions, such as if you follow people without doing analysis.</p>
                                <h2>Indicators to Pay Attention to in Crypto Technical Analysis</h2>
                                <p><br>There are various assessment criteria related to crypto technical analysis that you need to understand. This can help you in reading price movements. This is the explanation.</p>
                                <ul>
                                    <li>trend line</li>
                                    <li>Resistance dan support</li>
                                    <li>Moving average</li>
                                </ul>
                                <ol>
                                    <li><strong>Trendline</strong><br>This line is a line that you can draw yourself to connect the points of ups and downs in the price of a cryptocurrency that you choose.</li>
                                </ol>
                                <p>You will need this line for technical analysis as it helps predict future price movements.</p>
                                <p>You will find it easier to spot uptrends and downtrends. Even when there is a change in the trend, you can listen well.</p>
                                <ol>
                                    <li><strong>Resistance dan support</strong><br>In contrast to trend lines that follow the rising or falling of price movements, resistance and support lines are horizontal.</li>
                                </ol>
                                <p>You need to draw resistance and support together to map where the highest price is and where the lowest price is for a cryptocurrency .</p>
                                <p>When the price enters the support section , this is usually the time for traders to buy crypto assets. In contrast to resistance , this is usually used by traders to sell or withdraw profits.</p>
                                <ol>
                                    <li><strong>Moving average</strong><br>You calculate the average price of a crypto asset at any given time. For example, in 10 days, a crypto asset has an average price of IDR 40,000. The data that has been created can be connected into a line. This indicator is the EMA or can be called the Exponential Moving Average .</li>
                                </ol>
                                <h2>Crypto Price Patterns and Types</h2>
                                <p><br>Doing technical analysis will be very helpful when you understand what crypto price patterns are. These are the types.</p>
                                <ul>
                                    <li>Rising wedge</li>
                                    <li>Double top</li>
                                    <li>Cup dan handle</li>
                                    <li>Falling wedge</li>
                                </ul>
                                <ol>
                                    <li><strong>Rising wedge</strong><br>Is a chart pattern that shows the occurrence of a bearish or decline. The decline was initially marked by an increase for some time, then suddenly dropped until it touched the support point .</li>
                                    <li><strong>Double top</strong><br>It is a chart pattern that shows a bearish occurrence when the crypto price forms two hill patterns or an upward pattern , but after that it declines.</li>
                                    <li><strong>Cup dan handle</strong><br>There are two chart patterns going on here. The first pattern is the chart showing a curved cup-like shape, then forming the second pattern which continues to show an increase.</li>
                                    <li><strong>Falling wedge</strong><br>It is a graphic pattern when the price of a cryptocurrency experiences a deep decline, but after that the price slowly rises again. This pattern usually occurs when crypto prices are bullish .</li>
                                </ol>
                                <h2>What Affects Crypto Prices ?</h2>
                                <p><br>Factors that affect the price of this cryptocurrency can make you predict whether the price of crypto may rise or vice versa. This is the explanation.</p>
                                <ul>
                                    <li>Availability and demand for cryptocurrencies</li>
                                    <li>Its use as a payment tool</li>
                                    <li>Costs incurred for mining</li>
                                    <li>Cryptocurrency news globally</li>
                                    <li>Regulations set by the government</li>
                                </ul>
                                <ol>
                                    <li><strong>Availability and demand for cryptocurrencies</strong><br>The availability and demand for cryptocurrencies can make their prices increase or decrease.</li>
                                </ol>
                                <p>For example, when demand is high, while availability is low, the price of the crypto will increase.</p>
                                <ol>
                                    <li><strong>Its use as a means of payment</strong><br>Although not all of them, many countries have adopted cryptocurrencies as legal tender.</li>
                                </ol>
                                <p>The more a country makes crypto assets as a means of payment, it can make it go up. Vice versa.&nbsp;</p>
                                <ol>
                                    <li><strong>Costs incurred for mining</strong><br>Mining activities require separate tools, so the costs are high. This is also able to make the price of crypto go up.</li>
                                    <li><strong>Global cryptocurrency news</strong><br>Cryptocurrencies move on the basis of the sentiments that apply to them. When there is news of crypto fraud involving well-known developers, it can make many traders and investors save their assets. Finally, the price of crypto has decreased.</li>
                                    <li><strong>Regulations set by the government</strong><br>There are still many countries that enforce strict regulations regarding cryptocurrencies. One of them is Indonesia.</li>
                                </ol>
                                <p>Cryptocurrencies that apply globally are not necessarily licensed in Indonesia. You have to apply for a permit first. After BAPPEBTI allows it, then the crypto assets can be traded.</p>
                                <p>Regulations that are only set in one country can affect cryptocurrency prices globally.&nbsp;</p>
                                <h2>What is the Difference Between Coin and Token?</h2>
                                <p><br>The main difference between coins and tokens is the developer. The coin is developed by the developer who co-built the blockchain on which the coin resides. Whereas tokens are assets developed by one developer on a blockchain owned by another developer.</p>
                                <p>Although different, coins and tokens can both generate profits for their owners.</p>
                                <h2>Conclusion</h2>
                                <p><br>That’s the crypto technical analysis that you can know the basics. You will need to use technical analysis frequently in order to understand its use. The more you practice, the more proficient you will become.</p>
